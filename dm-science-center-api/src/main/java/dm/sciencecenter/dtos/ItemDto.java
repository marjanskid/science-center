package dm.sciencecenter.dtos;

import dm.sciencecenter.entities.Item;

public class ItemDto {

    public Long id;
    public String name;
    public double price;

    public ItemDto() {

    }

    public ItemDto(Long id, String name, double price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
